map<int, vector<int>> mp;
int power(int b, int e) {
    if (e == 0)
        return 1;
    if (e < 0)
        return 1 / power(b, -e);
    int temp = power(b, e / 2);
    if (e % 2 == 0)
        return temp * temp;
    else
        return b * temp * temp;
}
void primeFactors(int n)
{
    // vector<int> factors;
    int ct = 0;
    while (n % 2 == 0)
    {
        ct++;
        n = n / 2;
    }
    if (ct != 0)
    {
        mp[2].pb(ct);
    }
    for (int i = 3; i * i <= n; i = i + 2)
    {
        int ct = 0;
        while (n % i == 0)
        {
            ct++;
            n = n / i;
        }
        if (ct != 0)
        {
            mp[i].pb(ct);
        }
    }
    if (n > 2)
    {
        mp[n].pb(1);
    }
}
