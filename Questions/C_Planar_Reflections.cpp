void solve()
{
    int n,k;
    cin>>n>>k;
    viv dp(k+1,vi(n+1,0));
    for(int i=0;i<n;i++){
        dp[k-1][i]=1;
    }
    for(int i=k-2;i>=1;i--){
        if(i%2==k%2){
            int s=dp[i+1][n-1];
            for(int j=n-2;j>=0;j--){
                dp[i][j]=s;
                s=(s+dp[i+1][j])%mod;
            }
        }
        else{
            int s=dp[i+1][0];
            for(int j=1;j<n;j++){
                dp[i][j]=s;
                s=(s+dp[i+1][j])%mod;
            }
        }
    }
    int ans=1;
    for(int i=1;i<k;i++){
        for(int j=0;j<n;j++){
            ans=(ans+dp[i][j])%mod;
        }
    }
    cout<<ans<<endl;
}

