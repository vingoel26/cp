vi bst(1001,LLONG_MAX);
void solve()
{
    int n,k;
    cin>>n>>k;
    if(k==0){
        for(int i=1;i<=n;i++){
            cout<<i<<" ";
        }
        cout<<endl;
        return;
    }
    int k1=((n*(n-1))/2)-k;
    if(k1==0){
        for(int i=n;i>=1;i--){
            cout<<i<<" ";
        }
        cout<<endl;
        return;
    }
    if(bst[k1]>n){
        cout<<0<<endl;
        return;
    }
    int nd=n,st=0;
    for(int i=0;i<dp[k1].size();i++){
        st=nd-dp[k1][i]+1;
        for(int j=st;j<=nd;j++){
            cout<<j<<" ";
        }
        nd=st-1;
    }
    while(nd>0){
        cout<<nd<<" ";
        nd--;
    }
    cout<<endl;
}

