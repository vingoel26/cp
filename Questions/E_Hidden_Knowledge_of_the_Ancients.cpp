void solve()
{
    int n, k, l, r;
    cin >> n >> k >> l >> r;
    vi a(n + 1);
    for (int i = 1; i <= n; i++)
    {
        cin >> a[i];
    }
    map<int, int> m;
    int r1 = 1, l1 = 1;
    int ans = 0;
    map<int, int> m1;
    for (int i = 1; i <= n; i++)
    {
        m[a[i]]++;
        m1[a[i]]++;
        if (m.size() > k)
        {
            while (m.size() > k)
            {
                m[a[l1]]--;
                if (m[a[l1]] == 0)
                {
                    m.erase(a[l1]);
                }
                l1++;
            }
        }
        if (m1.size() >= k)
        {
            while (r1 <= i)
            {
                m1[a[r1]]--;
                if (m1[a[r1]] == 0)
                    m1.erase(a[r1]);

                if (m1.size() < k)
                {
                    m1[a[r1]] = 1;
                    break;
                }
                r1++;
            }
        }
        if (m.size() == k and m1.size() == k)
        {
            int l2 = i - r1 + 1;
            int r2 = i - l1 + 1;
            if (l2 > r or r2 < l)
            {
                continue;
            }
            l2 = max(l, l2);
            l2 = min(l2, r);
            r2 = min(r, r2);
            r2 = max(l, r2);
            ans += r2 - l2 + 1;
        }
    }
    cout << ans << endl;
}

