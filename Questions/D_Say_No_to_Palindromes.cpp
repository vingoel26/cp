void solve()
{
    int n,q;
    cin>>n>>q;
    string s;
    cin>>s;
    viv a(n+1,vi(6,0));
    vector<string> v={"abc","acb","bac","bca","cab","cba"};
    for(int i=0;i<6;i++){
        string s1="";
        for(int j=0;j<n/3+1;j++){
            s1+=v[i];
        }
        for(int j=1;j<=n;j++){
            if(s[j-1]==s1[j-1]){
                a[j][i]=a[j-1][i];
            }
            else{
                a[j][i]=a[j-1][i]+1;
            }
        }
    }
    for(int i=0;i<q;i++){
        int l,r;
        cin>>l>>r;
        int ans=1e9;
        for(int j=0;j<6;j++){
            ans=min(ans,a[r][j]-a[l-1][j]);
        }
        cout<<ans<<endl;
    }
}

