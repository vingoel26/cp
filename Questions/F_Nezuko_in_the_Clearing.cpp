void solve()
{
    int n,d;
    cin>>n>>d;
    int l=0,h=d,ans=2*d;
    while(l<=h){
        int mid=(l+h)/2;
        int s=(d+mid)/(mid+1),s1=s-1;
        int k=(s*(s+1))/2,k1=(s1*(s1+1))/2;
        int r=d%(mid+1);
        if(r==0){
            r=mid+1;
        }
        k=k*r;
        k1=k1*(mid+1-r);
        if(n-k-k1+mid>=1){
            ans=mid+r+s1*(mid+1);
            h=mid-1;
        }
        else{
            l=mid+1;
        }
    }
    cout<<ans<<endl;
}

