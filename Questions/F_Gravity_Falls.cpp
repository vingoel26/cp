void solve()
{
    int n;
    cin >> n;
    viv v(n);
    for (int i = 0; i < n; i++)
    {
        int k;
        cin >> k;
        v[i].resize(k);
        for (int j = 0; j < k; j++)
        {
            cin >> v[i][j];
        }
    }
    sort(v.begin(), v.end(), [](vector<int> &a, vector<int> &b)
         { return a.size() < b.size(); });

    int m = v[n - 1].size();
    vector<int> ans(m, 1e9);
    vector<int> b;
    b.push_back(0);

    for (int j = 0; j < n; j++)
    {
        int k = v[j].size();
        for (int e = 0; e < b.size(); e++)
        {
            int l = b[e], r;
            if (e + 1 < b.size())
            {
                r = b[e + 1];
            }
            else
            {
                r = k;
            }
            bool q = false;
            for (int i = l; i < r; i++)
            {
                if (ans[i] < v[j][i])
                {
                    break;
                }
                if (ans[i] > v[j][i])
                {
                    q = true;
                    break;
                }
            }
            if (q)
            {
                for (int i = l; i < k; i++)
                {
                    ans[i] = v[j][i];
                }
                while (b.back() > l)
                {
                    b.pop_back();
                }
                b.push_back(k);
                break;
            }
        }
    }
    for (int i = 0; i < ans.size(); i++)
    {
        cout << ans[i] << " ";
    }
    cout << endl;
}

