void solve()
{
    int n, k;
    cin >> n >> k;
    string s;
    cin >> s;
    int a = 0, b = 0, c = 0;
    string s1 = "";
    for (int i = 0; i < k; i++)
    {
        if (s[i] == '0')
        {
            a++;
        }
        if (s[i] == '1')
        {
            b++;
        }
        if (s[i] == '2')
        {
            c++;
        }
    }
    for (int i = 0; i < n; i++)
    {
        s1 += '+';
    }
    for (int i = 0; i < a; i++)
    {
        s1[i] = '-';
    }
    for (int i = n - 1; i > n - 1 - b; i--)
    {
        s1[i] = '-';
    }
    for (int i = a; i < a + c; i++)
    {
        if (s1[i] == '+')
        {
            s1[i] = '?';
        }
    }
    for (int i = n - b - 1; i >= n - b - c; i--)
    {
        if (s1[i] == '+')
        {
            s1[i] = '?';
        }
    }
    if (a + c >= n or b + c >= n or k>=n)
    {
        for (int i = 0; i < n; i++)
        {
            cout << '-';
        }
        cout << endl;
        return;
    }
    cout << s1 << endl;
}

