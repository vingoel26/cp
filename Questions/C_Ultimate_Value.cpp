void solve()
{
    int n;
    cin >> n;
    vi a(n);
    vin(a);
    if (n == 1)
    {
        cout << a[0] << endl;
        return;
    }
    int s1 = 0, s2 = 0;
    for (int i = 0; i < n; i = i + 2)
    {
        s1 += a[i];
    }
    for (int i = 1; i < n; i = i + 2)
    {
        s2 += a[i];
    }
    int ans = s1 - s2;
    int ans1 = 0;
    for (int i = 0; i < n; i++)
    {
        ans1 = max(ans1,i - (i % 2));
    }
    int mne = 1e18, mno = 1e18;
    for (int i = 0; i < n; i++)
    {
        if (i % 2)
        {
            ans1 = max(i + 2*a[i] - mne, ans1);
            mno = min(mno, i - 2*a[i]);
        }
        else
        {
            ans1 = max(i -2*a[i] - mno, ans1);
            mne = min(mne, i +2*a[i]);
        }
    }

    cout << ans +ans1<< endl;
}

