void solve()
{
    int n;
    cin>>n;
    vi a(2*n);
    for(int i=0;i<2*n;i++){
        cin>>a[i];
    }
    sort(all(a));
    int s1=0,s2=0;
    for(int i=0;i<2*n-1;i++){
        if(i%2==0){
            s1+=a[i+1]-a[i];
        }
        else{
            s2+=a[i+1]-a[i];
        }
    }
    int ans=s1;
    int s=a[2*n-1]-a[0];
    for(int i=0;i<n;i++){
        cout<<ans<<" ";
        s-=a[i+1]-a[i];
        s-=a[2*n-i-1]-a[2*n-i-2];
        ans+=2*s2;
        s2=s-s2;
    }
    cout<<endl;
}

