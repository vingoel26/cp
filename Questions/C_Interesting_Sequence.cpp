void solve()
{

    int n, x;
    cin >> n >> x;
    if ((x & n) != x) {
        cout << -1 << endl;
        return;
    }
    if (x == n) {
        cout << n << endl;
        return;
    }
    int ans = n, ans1 = LLONG_MAX;
    for (int i = 0; i < 64; ++i) {
        bool n1 = (n >> i) & 1;
        bool x1 = (x >> i) & 1;
        if (!x1 && n1) {
            int mi = ((n >> (i + 1)) + 1) << (i + 1);
            ans = max(ans, mi);
        }
        if (x1) {
            int mi = ((n >> i) << i) + ((1LL << i) - 1);
            ans1 = min(ans1, mi);
        }
    }
    if (ans < ans1) {
        cout << ans << endl;
    } else {
        cout << -1 << endl;
    }
}

