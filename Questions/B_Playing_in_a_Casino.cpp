void solve()
{
    int m, n;
    cin >> n >> m;
    vector<vector<int>> a(n, vector<int>(m));
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < m; j++)
        {
            cin >> a[i][j];
        }
    }
    int ans = 0;
    for (int i = 0; i < m; i++)
    {
        int w = n - 1;
        vi b;
        for (int j = 0; j < n; j++)
        {
            b.push_back(a[j][i]);
        }
        sort(rall(b));
        for (int j = 0; j < n; j++)
        {
            ans += b[j] * w;
            w -= 2;
            // cout << ans << ' ';
        }
        // cout << endl;
        // cout << ans << ' ';
        // cout << endl;
    }
    cout << ans << endl;
}

