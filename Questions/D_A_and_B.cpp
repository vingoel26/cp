void solve()
{
    int n;
    cin>>n;
    string s;
    cin>>s;
    vi a,b;
    for(int i=0;i<n;i++){
        if(s[i]=='a'){
            a.pb(i);
        }
        else{
            b.pb(i);
        }
    }
    int cta=0,ctb=0;
    int m1=a.size()/2,m2=b.size()/2;
    for(int i=0;i<a.size();i++){
        cta+=abs(a[m1]-a[i])-abs(m1-i);
    }
    for(int i=0;i<b.size();i++){
        ctb+=abs(b[m2]-b[i])-abs(m2-i);
    }
    cout<<min(cta,ctb)<<endl;
}

