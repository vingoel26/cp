void solve()
{
    string s;
    int n, m;
    cin >> s >> m;
    n = s.size();
    m--;
    int x = n, k = 0;
    while (m >= x)
    {
        m -= x;
        x--;
        k++;
    }
    string s1 = "";
    s1.push_back(s[0]);
    int i = 1;
    for (i = 1; i < n; i++)
    {
        if (k == 0)
        {
            break;
        }
        while (s1.size()>0 and s1[s1.size() - 1] > s[i])
        {
            s1.pop_back();
            k--;
            if (k == 0)
            {
                break;
            }
        }
        s1.push_back(s[i]);
    }
    for (i; i < n; i++)
    {
        s1.push_back(s[i]);
    }
    cout << s1[m];
}

