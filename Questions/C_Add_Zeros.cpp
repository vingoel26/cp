bool comp(pair<int, int> a, pair<int, int> b)
{
    if (a.ff == b.ff)
    {
        return a.ss < b.ss;
    }
    return a.ff < b.ff;
}
void solve()
{
    int n;
    cin >> n;
    vi a(n);
    vin(a);
    int k = n;
    vector<pair<int, int>> s;
    map<int,int> ct;
    for (int i = 0; i < n; i++)
    {
        a[i] = a[i] + i;
        if (a[i] >= n)
        {
            s.pb({a[i], i});
        }
        // ct[a[i]]++;
    }
    ct[n]++;
    sort(all(s));
    for(int i =0;i<s.size();i++){
        if(ct[s[i].first]>=1){
            ct[s[i].first+s[i].second]++;
            k=max(k,s[i].first+s[i].second);
        }
    }
    cout<<k<<endl;
}

