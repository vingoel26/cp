void solve()
{
    int n;
    cin>>n;
    vi a(n),b(n);
    vin(a);
    vin(b);
    vi a1(n),b1(n);
    for(int i=0;i<n;i++){
        a1[i]=min(a[i],b[i]);
        b1[i]=max(a[i],b[i]);
    }
    vi dp(2);
    dp[0]=1;
    dp[1]=1;
    for(int i=1;i<n;i++){
        int z=0,o=0;
        if(a1[i]>=a1[i-1] and b1[i]>=b1[i-1]){
            z=(z+dp[0])%mod;
            o=(o+dp[1])%mod;
        }
        if(a1[i]>=b1[i-1] and b1[i]>=a1[i-1]){
            z=(z+dp[1])%mod;
            o=(o+dp[0])%mod;
        }
        dp[0]=z;
        dp[1]=o;
    }
    cout<<(dp[0]+dp[1])%mod<<endl;
}

