vi d(1001,1001);
void solve()
{
    int n,k;
    cin>>n>>k;
    vi a(n),b(n);
    vin(a);
    vin(b);
    int s1=accumulate(all(b),0LL),s2=0;;
    for(int i=0;i<n;i++){
        s2+=d[a[i]];
    }
    if(s2<=k){
        cout<<s1<<endl;
        return;
    }
    viv dp(n+1,vi(k+1,0));
    for(int i=1;i<=n;i++){
        for(int j=0;j<=k;j++){
            if(j>=d[a[i-1]]){
                dp[i][j]=max(dp[i-1][j],dp[i-1][j-d[a[i-1]]]+b[i-1]);
            }
            else{
                dp[i][j]=dp[i-1][j];
            }
        }
    }
    int ans=0;
    for(int i=0;i<=k;i++){
        ans=max(dp[n][i],ans);
    }
    cout<<ans<<endl;
}

