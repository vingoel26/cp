void solve()
{
    int n, k;
    cin >> n >> k;
    vi a(n);
    vin(a);
    int mx = *max_element(all(a));
    int l = mx, r = mx + k;
    int ans = 0;
    while (l <= r)
    {
        int m = (l + r) / 2;
        bool q=false;
        for (int i = 0; i < n; i++)
        {
            int ct = 0, m1 = m, p = i;
            for (int j = i; j < n; j++)
            {
                if (a[j] < m1)
                {
                    ct += m1 - a[j];
                    m1--;
                    p++;
                }
                else
                {
                    break;
                }
            }
            if (p < n and ct <= k)
            {
                q=true;
            }
        }
        if(q){
            ans=m;
            l=m+1;
        }
        else
        {
            r = m - 1;
        }
    }
    cout << ans << endl;
}

