void solve()
{
    int n,y;
    cin>>n>>y;
    vi a(n);
    int mx=0;
    for(int i=0;i<n;i++){
        cin>>a[i];
        mx=max(mx,a[i]);
    }
    vi mp(mx + 2, 0), pr(mx + 2, 0);
    for (int i = 0; i < n; i++) {
        mp[a[i]]++;
    }
    for (int i = 1; i <= mx; i++) {
        pr[i] = pr[i - 1] + mp[i];
    }
    int ans=-1e18;
    for(int i=2;i<=mx+1;i++){
        int n1=(mx+i-1)/i;
        int ct=0,ct1=0;
        for(int j=1;j<=n1;j++){
            int l=(j-1)*i+1;
            int r=j*i;
            if(l>mx) break;
            r=min(r,mx);
            ct1+=(pr[r]-pr[l-1])*j;
            ct+=min(mp[j],pr[r]-pr[l-1]);
        }
        int cost=(n-ct)*y;
        ans=max(ans,ct1-cost);
    }
    cout<<ans<<endl;
}

