void solve()
{
    int n,q;
    cin>>n>>q;
    vi a(n);
    vin(a);
    vi ct0(n+1,0),ct1(n+1,0);
    for(int i=1;i<=n;i++){
        if(a[i-1]==0){
            ct0[i]=ct0[i-1]+1;
        }
        else{
            ct0[i]=ct0[i-1];
        }
    }
    for(int i=1;i<=n;i++){
        if(a[i-1]==1){
            ct1[i]=ct1[i-1]+1;
        }
        else{
            ct1[i]=ct1[i-1];
        }
    }
    vi ct(n+1,0),pr(n+1,0);
    for(int i=1;i<=n-1;i++){
        if(a[i-1]==a[i]){
            ct[i]=1;
        }
        else{
            ct[i]=0;
        }
    }
    for(int i=1;i<=n;i++){
        pr[i]=pr[i-1]+ct[i];
    }
    for(int i=0;i<q;i++){
        int l,r;
        cin>>l>>r;
        int ln=r-l+1;
        int z=ct0[r]-ct0[l-1];
        int o=ct1[r]-ct1[l-1];
        if(z%3!=0 or o%3!=0){
            cout<<-1<<endl;
            continue;
        }
        if(l!=r){
            if(pr[r-1]-pr[l-1]!=0){
                cout<<ln/3<<endl;
                continue;
            }
        }
        cout<<ln/3+1<<endl;
    }
}

